//В школу бальных танцев записались N учеников — мальчиков и девочек.Профессор построил их в один ряд, 
// и хочет отобрать из них для первого занятия группу стоящих подряд учеников, в которой количество мальчиков и девочек одинаково.Сколько вариантов выбора есть у профессора? 
// Решить задачу с помощью рекуррентных соотношений.
//Ввод.В первой строке входного файла задано число N(1 ≤ N ≤ 106).Во второй строке задается описание построенного ряда из мальчиков и девочек — строка из N символов a и b(символ a соответствует девочке, а символ b — мальчику).
//Вывод.В единственной строке выходного файла должно содержаться количество вариантов выбора требуемой группы.
//Примеры
//Ввод 1         Ввод 2
//3              8
//bab            abbababa
//Вывод 1        Вывод 2
//2              13

#include <iostream>
#include <vector>
#include <unordered_map>

using namespace std;

int main() {
    int N;
    cin >> N;
    string students;
    cin >> students;

    // dp[diff] будет содержать количество вариантов для данной разницы между количеством мальчиков и девочек
    unordered_map<int, long long> dp;
    dp[0] = 1; // Базовый случай: разница 0, когда количество мальчиков и девочек одинаково

    int balance = 0; 
    long long answer = 0;

    for (char student : students) {
        if (student == 'a') {
            balance++; 
        }
        else {
            balance--; 
        }

        answer += dp[balance];

        dp[balance]++;
    }

    cout << answer << endl;

    return 0;
}